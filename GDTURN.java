import java.util.*;



public class CodeChef1 {


public static void main(String args[]){

    try (Scanner input = new Scanner(System.in)) {
        int T=input.nextInt();
        for (int i = 0; i < T; i++) {
            int X=input.nextInt();
            int Y=input.nextInt();
            if(X+Y>6){
                System.out.println("YES");
            }
            else{
                System.out.println("NO");
            }
            
        }
    }

    



}
    
}
